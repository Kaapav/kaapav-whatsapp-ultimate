/**
 * Detect language from text (basic detection)
 */
export function detectLanguage(text) {
  if (!text) return 'en';

  // Check for Hindi characters (Devanagari)
  if (/[\u0900-\u097F]/.test(text)) {
    return 'hi';
  }

  // Check for Kannada characters
  if (/[\u0C80-\u0CFF]/.test(text)) {
    return 'kn';
  }

  // Check for common Hindi transliterated words
  const hindiWords = ['kya', 'hai', 'kaise', 'aur', 'mujhe', 'chahiye', 'kitna', 'kahan'];
  const lowerText = text.toLowerCase();
  for (const word of hindiWords) {
    if (lowerText.includes(word)) return 'hi';
  }

  // Default to English
  return 'en';
}

