/**
 * Translate text from English to target language
 * YOUR ORIGINAL FUNCTION - PRESERVED
 * @param {string} text - English text to translate
 * @param {string} targetLang - Target language code (en, hi, kn)
 * @returns {string} - Translated text
 */
export async function fromEnglish(text, targetLang = 'en') {
  // Return original if English or no translation needed
  if (!text || targetLang === 'en') {
    return text;
  }

  // Normalize language code
  const lang = targetLang.toLowerCase().trim();
  if (!['hi', 'kn'].includes(lang)) {
    return text;
  }

  // Check direct translation
  if (TRANSLATIONS[text] && TRANSLATIONS[text][lang]) {
    return TRANSLATIONS[text][lang];
  }

  // Try partial matching for longer texts
  let translatedText = text;
  
  // Sort by length (longer phrases first) to avoid partial replacements
  const sortedPhrases = Object.keys(TRANSLATIONS).sort((a, b) => b.length - a.length);
  
  for (const english of sortedPhrases) {
    const translations = TRANSLATIONS[english];
    if (translations[lang] && translatedText.includes(english)) {
      translatedText = translatedText.replace(new RegExp(escapeRegex(english), 'g'), translations[lang]);
    }
  }

  return translatedText;
}

/**
 * Translate text to English from source language
 * @param {string} text - Text in source language
 * @param {string} sourceLang - Source language code
 * @returns {string} - English text
 */
export async function toEnglish(text, sourceLang = 'en') {
  if (!text || sourceLang === 'en') {
    return text;
  }

  // Reverse lookup
  for (const [english, translations] of Object.entries(TRANSLATIONS)) {
    if (translations[sourceLang] === text) {
      return english;
    }
  }

  return text;
}

/**
 * Escape regex special characters
 */
function escapeRegex(string) {
  return string.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

